/**
 * Material Design 3 token generator.
 *
 * Reads SEED_COLOR (hex) and emits CSS custom properties for the full
 * MD3 color system (light + dark) plus tonal palettes. Run via:
 *
 *   node scripts/generate-md3-tokens.mjs
 *
 * Re-run after changing SEED_COLOR to regenerate css/md3-tokens.css.
 */
import { writeFileSync } from 'node:fs'
import { argbFromHex, hexFromArgb, themeFromSourceColor } from '@material/material-color-utilities'

const SEED_COLOR = '#006C46' // MD3 green — change to re-skin
const OUT_PATH = new URL('../css/md3-tokens.css', import.meta.url)

const theme = themeFromSourceColor(argbFromHex(SEED_COLOR))

const hex = (argb) => hexFromArgb(argb)
const tone = (palette, t) => hex(palette.tone(t))

// Surface container tones per M3 spec (palettes.neutral)
const surface = {
  light: {
    surface: tone(theme.palettes.neutral, 98),
    'surface-dim': tone(theme.palettes.neutral, 87),
    'surface-bright': tone(theme.palettes.neutral, 98),
    'surface-container-lowest': tone(theme.palettes.neutral, 100),
    'surface-container-low': tone(theme.palettes.neutral, 96),
    'surface-container': tone(theme.palettes.neutral, 94),
    'surface-container-high': tone(theme.palettes.neutral, 92),
    'surface-container-highest': tone(theme.palettes.neutral, 90),
  },
  dark: {
    surface: tone(theme.palettes.neutral, 6),
    'surface-dim': tone(theme.palettes.neutral, 6),
    'surface-bright': tone(theme.palettes.neutral, 24),
    'surface-container-lowest': tone(theme.palettes.neutral, 4),
    'surface-container-low': tone(theme.palettes.neutral, 10),
    'surface-container': tone(theme.palettes.neutral, 12),
    'surface-container-high': tone(theme.palettes.neutral, 17),
    'surface-container-highest': tone(theme.palettes.neutral, 22),
  },
}

const schemeToVars = (scheme, override) => {
  const map = {
    primary: scheme.primary,
    'on-primary': scheme.onPrimary,
    'primary-container': scheme.primaryContainer,
    'on-primary-container': scheme.onPrimaryContainer,
    secondary: scheme.secondary,
    'on-secondary': scheme.onSecondary,
    'secondary-container': scheme.secondaryContainer,
    'on-secondary-container': scheme.onSecondaryContainer,
    tertiary: scheme.tertiary,
    'on-tertiary': scheme.onTertiary,
    'tertiary-container': scheme.tertiaryContainer,
    'on-tertiary-container': scheme.onTertiaryContainer,
    error: scheme.error,
    'on-error': scheme.onError,
    'error-container': scheme.errorContainer,
    'on-error-container': scheme.onErrorContainer,
    background: scheme.background,
    'on-background': scheme.onBackground,
    'on-surface': scheme.onSurface,
    'surface-variant': scheme.surfaceVariant,
    'on-surface-variant': scheme.onSurfaceVariant,
    outline: scheme.outline,
    'outline-variant': scheme.outlineVariant,
    shadow: scheme.shadow,
    scrim: scheme.scrim,
    'inverse-surface': scheme.inverseSurface,
    'inverse-on-surface': scheme.inverseOnSurface,
    'inverse-primary': scheme.inversePrimary,
  }
  const allVars = { ...map, ...override }
  return Object.entries(allVars)
    .map(([k, v]) => `  --md-sys-color-${k}: ${typeof v === 'string' ? v : hex(v)};`)
    .join('\n')
}

const palettes = {
  primary: theme.palettes.primary,
  secondary: theme.palettes.secondary,
  tertiary: theme.palettes.tertiary,
  neutral: theme.palettes.neutral,
  'neutral-variant': theme.palettes.neutralVariant,
  error: theme.palettes.error,
}
const TONES = [0, 10, 20, 25, 30, 35, 40, 50, 60, 70, 80, 90, 95, 98, 99, 100]
const tonalPalettesCss = Object.entries(palettes)
  .flatMap(([name, p]) => TONES.map((t) => `  --md-ref-palette-${name}-${t}: ${tone(p, t)};`))
  .join('\n')

// Map Tailwind primary-{50..950} & gray-{50..950} to MD3 tonal scale so existing
// components automatically pick up the MD3 palette without class renames.
const tailwindShadeMap = [
  [50, 95],
  [100, 90],
  [200, 80],
  [300, 70],
  [400, 60],
  [500, 50],
  [600, 40],
  [700, 30],
  [800, 20],
  [900, 10],
  [950, 0],
]
const tailwindShadeVars = (name, palette) =>
  tailwindShadeMap
    .map(([shade, t]) => `  --color-${name}-${shade}: ${tone(palette, t)};`)
    .join('\n')

const themeCss = `@theme {
${tailwindShadeVars('primary', theme.palettes.primary)}
${tailwindShadeVars('gray', theme.palettes.neutral)}
}`

const css = `/* Auto-generated by scripts/generate-md3-tokens.mjs — do not edit by hand. */
/* Seed color: ${SEED_COLOR} */

:root {
${schemeToVars(theme.schemes.light, surface.light)}
${tonalPalettesCss}
}

.dark {
${schemeToVars(theme.schemes.dark, surface.dark)}
}

${themeCss}
`

writeFileSync(OUT_PATH, css)
console.log(`Wrote ${OUT_PATH.pathname}`)
